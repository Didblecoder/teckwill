    public void addRowToJTable() {
        DefaultTableModel model = (DefaultTableModel) tabelVar.getModel();

        EmployeeService employee = new EmployeeService();

        ArrayList<Employee> list = employee.ShowList();
        Object rowData[] = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            
            
            rowData[0] = i+1;
            rowData[1] = list.get(i).getNume();
            rowData[2] = list.get(i).getPrenume();
            rowData[3] = list.get(i).getDataNasteri();
            rowData[4] = list.get(i).getSpecialitate();
               
            model.addRow(rowData);
            
        }

    }

