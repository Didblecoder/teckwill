    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
       try{ int index = tabelVar.getSelectedRow();
        
        DefaultTableModel modelForEdit = (DefaultTableModel) tabelVar.getModel();
        //TableModel modelForEdit = tabelVar.getModel();
        
        String eNume = modelForEdit.getValueAt(index, 1).toString();
        String ePrenume = modelForEdit.getValueAt(index, 2).toString();
        Date date = null;
        try {
             date = new SimpleDateFormat("dd/MM/yyyy").parse((String)modelForEdit.getValueAt(index, 3));
           // JDateChooser.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(MainViewPannel.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        String eSpecialitate = modelForEdit.getValueAt(index, 4).toString();
        //  Object eSpecialitate = modelForEdit.getValueAt(index, 3);
          
          
        jtRowData.setVisible(true);
        jtRowData.pack();
        jtRowData.setLocationRelativeTo(null);
        jtRowData.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        jtRowData.numeEditVar.setText(eNume);
        jtRowData.prenumeEditVar.setText(ePrenume);
        jtRowData.dateBirthEditVar.setDate(date);
        jtRowData.functiaEditVar.setSelectedItem(eSpecialitate);
        jtRowData.indexEdit = index;
       
        
        this.dispose();
        
        }
       catch(Exception ex){
       JOptionPane.showMessageDialog(rootPane, "Select first the employee which you want to edit");
       
       }
        
        
        
//        new EditViewPannel().setVisible(true);
    }//GEN-LAST:event_editActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        DefaultTableModel modelForDelete = (DefaultTableModel) tabelVar.getModel();
        try {
            int SelectedRowIndex = tabelVar.getSelectedRow();
            modelForDelete.removeRow(SelectedRowIndex);
            EmployeeService.RemoveFromList(SelectedRowIndex);
            this.dispose();
            new MainViewPannel().setVisible(true);
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(rootPane, "Select first the employee which you want to delete");
        }


    }//GEN-LAST:event_deleteButtonActionPerformed

